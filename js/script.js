// plant 1 
document.getElementById('plant-1').addEventListener('click', function(){
    const productImg = document.getElementById('product-img');
    productImg.src = '../images/product-11.jpg';
    const productName = document.getElementById('product-name');
    const plantName = document.getElementById('plant-1-name');
    productName.innerText = plantName.innerText;
    const productPrice = document.getElementById('product-price');
    const plantPrice = document.getElementById('plant-1-price');
    productPrice.innerText = plantPrice.innerText;
});

